/**
 * @param {string} src
 * @returns {Promise<HTMLImageElement>}
 */
function loadImage(src) {
  return new Promise((resolve) => {
    const img = new Image();
    img.src = src;
    img.addEventListener("load", () => resolve(img));
  });
}

/**
 *
 * @param {number} n
 * @param {number} ratio
 */
function jitter(n, ratio = 0.5) {
  return n + (Math.random() * (ratio * 2) - ratio);
}

const perspective = 100;
const slides = 10;

/**
 * @typedef {Object} FlowerData
 * @property {number} id
 * @property {number} x
 * @property {number} y
 * @property {number} z
 * @property {number} scale
 * @property {number} rotate
 */

/**
 * @param {number} count
 * @param {number} top
 * @param {number} bottom
 * @param {number} iterations
 * @returns {FlowerData[]}
 */
function createFlowerData(count, top, bottom, iterations) {
  const flowers = [];
  let z = 0;
  for (let iteration = 0; iteration < iterations; iteration++) {
    const ids = new Array(count)
      .fill(true)
      .map((_, i) => i)
      .sort(() => (Math.random() > 0.5 ? 1 : -1));
    for (let i = 0; i < count; i++) {
      const scale = (perspective - z) / perspective;
      let x = Math.random() * 100;
      if (ids[i] === 3) {
        x = Math.random() < 0.5 ? 0 : 100;
      }
      flowers.push({
        id: ids[i],
        x,
        y: top + Math.random() * (bottom - top),
        z: jitter(z),
        scale: jitter(scale * 1.3),
        rotate: x === 0 ? 180 : 0,
      });
      z += 1;
    }
  }
  return flowers;
}

/**
 * @param {HTMLImageElement[]} images
 * @param {FlowerData[]} flowers
 * @param {number} screenHeight
 */
function positionFlowers(images, flowers, screenHeight) {
  for (const { id, x, y, z, scale, rotate } of flowers) {
    const { width, height, src } = images[id];
    const w = width / 2;
    const h = height / 2;
    const img = document.createElement("img");
    img.className = "flower";
    img.src = src;
    img.style.width = `${w}px`;
    img.style.height = `${h}px`;
    img.style.left = `${-w / 2}px`;
    img.style.transform = `translate3d(calc(${x}vw - ${w / 2}px), ${
      y * screenHeight - h / 2
    }px, ${jitter(z)}px) scale(${scale}) rotateZ(${rotate}deg)`;
    content?.appendChild(img);
  }
}

