/**
 * @param {string} src
 * @returns {Promise<HTMLImageElement>}
 */
function loadImage(src) {
  return new Promise((resolve) => {
    const img = new Image();
    img.src = src;
    img.addEventListener("load", () => resolve(img));
  });
}

/**
 *
 * @param {number} n
 * @param {number} ratio
 */
function jitter(n, ratio = 0.5) {
  return n + (Math.random() * (ratio * 2) - ratio);
}

const perspective = 100;
